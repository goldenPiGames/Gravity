const SPRITES_LOADED = {};

function getSpriteSheet(name) {
	if (!SPRITES_LOADED[name]) {
		let dat = SPRITE_DATA[name];
		SPRITES_LOADED[name] = new SpriteSheet("src/Sprites/"+name+"."+(dat._ext||"png"), dat, true);
	}
	return SPRITES_LOADED[name];
}